    List<String>  taskIdList = new ArrayList();
    private void taskTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_taskTableMouseClicked
        taskIdList.clear();
        for(  Integer i: taskTable.getSelectedRows() ) {
            taskIdList.add( taskTable.getValueAt( i, 0 ) + "" );
        }
    }//GEN-LAST:event_taskTableMouseClicked

    private void sectionManualUpdateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sectionManualUpdateButtonActionPerformed
       messageLabel.setText( "正在执行" );

       //关闭自动更新
       if( timer != null ) {
            autoUpdateButton.setText( "启动" );
            timer.cancel();
       }
       for( String id: taskIdList ) {
           System.out.print( id + " " );
       }
       System.out.println();
       messageLabel.setText( "部分执行完成执行" );
    }//GEN-LAST:event_sectionManualUpdateButtonActionPerformed

