    Timer timer = null;
    private void autoUpdateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_autoUpdateButtonActionPerformed
        if( autoUpdateButton.getText().equals( "启动" ) ) {
            timer = new Timer();
            PropertyReader pr = new PropertyReader();
            long period = Long.valueOf( updateIntervalTextField.getText() ) * 1000 * 60;
            timer.scheduleAtFixedRate( new MainTimerTask( updateIntervalTextField.getText() ), new Date(), period );
            autoUpdateButton.setText( "停止" );
            messageLabel.setText( "已启动" );
        }
        else {
            autoUpdateButton.setText( "启动" );
            timer.cancel();
            messageLabel.setText( "已停止" );
        }      
}//GEN-LAST:event_autoUpdateButtonActionPerformed

